//Dias de la semana switch case
let diaSemana = 10;

switch (diaSemana) {
  case 1:
    console.log("Hoy es lunes");
    break;
  case 2:
    console.log("Hoy es Martes");
    break;
  case 3:
    console.log("Hoy es Miercoles");
    break;
  case 4:
    console.log("Hoy es Jueves");
    break;
  case 5:
    console.log("Hoy es Viernes");
    break;
  case 6:
    console.log("Hoy es Viernes");
    break;
  default:
    console.log(`Dia de semana erroneo ${diaSemana}`);
}
